def expand_synonyms(text: str) -> str:
    """Если в тексте встречается любой из синонимов — добавить остальные."""
    if not text:
        return text
    t = text.lower()
    additions: set[str] = set()
    for syns in PRODUCT_SYNONYMS.values():
        if any(s in t for s in syns):
            additions.update(syns)
    if not additions:
        return text
    return f"{text} {' '.join(sorted(additions))}"
